package com.amro.movies.domain.model

/**
 * Represents the direction of a sorting operation.
 */
enum class SortDirection {
    /**
     * Sort in ascending order (e.g., A to Z, oldest to newest, lowest to highest).
     */
    ASCENDING,

    /**
     * Sort in descending order (e.g., Z to A, newest to oldest, highest to lowest).
     */
    DESCENDING
}
